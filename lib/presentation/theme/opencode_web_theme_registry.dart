@immutable
class OpenCodeThemePaletteData {
  const OpenCodeThemePaletteData({required this.neutral, required this.ink, required this.primary, required this.success, required this.warning, required this.error, required this.info, this.accent, this.interactive, this.diffAdd, this.diffDelete});
